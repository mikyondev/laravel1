	 * PUT /posts/{id}
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id)
	{
        $post = Post::findOrFail($id);
        $v = Validator::make(Input::all(), Post::$rules);

        if($v->fails())
        {
            return $v->errors()->toJson();
        }
        else
        {
            $post->update(Input::all());
            return $post->toJson();
        }
	}

